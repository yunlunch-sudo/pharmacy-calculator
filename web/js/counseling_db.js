/**
 * 약품명으로 복약지도 정보를 검색합니다.
 * @param {string} drugName - 검색할 약품명
 * @returns {object|null} 복약지도 정보 또는 null
 */
function findCounseling(drugName) {
    if (!drugName) return null;
    const name = drugName.toLowerCase();
    return COUNSELING_DB.find(c =>
        c.keywords.some(kw => drugName.includes(kw))
    ) || null;
}
