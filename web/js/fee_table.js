function getDaysKey(days) {
    for (const [low, high] of DAYS_RANGES) {
        if (days >= low && days <= high) {
            if (low === high) return String(low);
            if (high === 999) return "91이상";
            return `${low}~${high}`;
        }
    }
    throw new Error(`유효하지 않은 투약일수: ${days}`);
}

